#include "ft_printf.h"

/*
%p (nil) null pointer 
%s (null) null string
printf convention 
*/

int	ft_print_string(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
